// Fungsi untuk konversi dari Celsius ke Reaumur
double convertToReaumur(double celsius) {
  return celsius * 4 / 5;
}

// Fungsi untuk konversi dari Celsius ke Fahrenheit
double convertToFahrenheit(double celsius) {
  return (celsius * 9 / 5) + 32;
}

// Fungsi untuk konversi dari Celsius ke Kelvin
double convertToKelvin(double celsius) {
  return celsius + 273.15;
}
